function resolveLocale() {
    const saved = localStorage.getItem('userLanguage');
    if (saved && STRINGS[saved]) {
        return saved;
    }
    
    const language = (navigator.language || 'en').toLowerCase();
    for (const key of Object.keys(STRINGS)) {
        if (language.startsWith(key)) return key;
    }
    return 'en';
}

function t(key) {
    const locale = resolveLocale();
    return STRINGS[locale][key] || STRINGS.en[key] || '';
}

function updateStatusBanner(enabled, suffix) {
    const statusBanner = document.querySelector('.status-banner');
    if (!statusBanner) {
        return;
    }

    let statusKey = `statusUnknown${suffix}`;
    if (enabled === true) {
        statusKey = `statusOn${suffix}`;
    } else if (enabled === false) {
        statusKey = `statusOff${suffix}`;
    }
    statusBanner.textContent = t(statusKey);
}

function applyLocalizedCopy(useSettingsInsteadOfPreferences) {
    document.documentElement.lang = resolveLocale() === 'zh' ? 'zh-CN' : 'en';

    document.querySelectorAll('[data-i18n]').forEach((element) => {
        let key = element.dataset.i18n;
        if (key === 'iosUsageTitle') {
            if (window.currentPlatform === 'iPhone') key = 'iphoneUsageTitle';
            if (window.currentPlatform === 'iPad') key = 'ipadUsageTitle';
        }
        
        if (key === 'statusUnknownSettings' || key === 'statusOnSettings' || key === 'statusOffSettings') {
            return;
        }
        element.innerHTML = t(key).replace('Safari', '<strong>Safari</strong>').replace('Extensions', '<strong>Extensions</strong>').replace('Dark Light', '<strong>Dark Light</strong>').replace('On', '<strong>On</strong>').replace('Allow', '<strong>Allow</strong>');
        // Actually, innerHTML replace is risky. Let's just use textContent unless it's a step with bolding.
        if (key.startsWith('iosStep') || key.startsWith('macStep')) {
            element.innerHTML = t(key).replace(/Safari/g, '<strong>Safari</strong>')
                                      .replace(/Extensions/g, '<strong>Extensions</strong>')
                                      .replace(/Dark Light/g, '<strong>Dark Light</strong>')
                                      .replace(/ On\./g, ' <strong>On</strong>.')
                                      .replace(/ Allow\./g, ' <strong>Allow</strong>.')
                                      .replace(/扩展/g, '<strong>扩展</strong>')
                                      .replace(/暗光/g, '<strong>暗光</strong>')
                                      .replace(/允许/g, '<strong>允许</strong>');
        } else {
            element.textContent = t(key);
        }
    });

    document.querySelectorAll('[data-i18n-attr]').forEach((element) => {
        const mappings = element.dataset.i18nAttr.split(',');
        mappings.forEach((mapping) => {
            const [attribute, key] = mapping.split(':');
            if (!attribute || !key) {
                return;
            }
            element.setAttribute(attribute.trim(), t(key.trim()));
        });
    });

    const suffix = useSettingsInsteadOfPreferences ? 'Settings' : 'Preferences';
    updateStatusBanner(null, suffix);
    document.querySelector('.open-preferences').textContent = t(`openPreferences${suffix}`);
    document.title = t('pageTitle');
}

function show(enabled, useSettingsInsteadOfPreferences, platform) {
    // Determine platform, default to macOS if not provided
    window.currentPlatform = platform || 'macOS';
    const isIOS = (platform === 'iOS' || platform === 'iPhone' || platform === 'iPad');
    
    if (isIOS) {
        document.getElementById('ios-guide').style.display = 'block';
        document.getElementById('mac-guide').style.display = 'none';
    } else {
        document.getElementById('mac-guide').style.display = 'block';
        document.getElementById('ios-guide').style.display = 'none';
    }

    applyLocalizedCopy(useSettingsInsteadOfPreferences);

    const suffix = useSettingsInsteadOfPreferences ? 'Settings' : 'Preferences';
    updateStatusBanner(enabled, suffix);

    if (typeof enabled === "boolean") {
        document.body.classList.toggle(`state-on`, enabled);
        document.body.classList.toggle(`state-off`, !enabled);
        
        const openSafariBtn = document.querySelector('.open-safari');
        const openPrefsBtn = document.querySelector('.open-preferences');
        if (enabled) {
            openSafariBtn?.style.removeProperty('display');
            openSafariBtn?.classList.remove('secondary-action');
            openSafariBtn?.classList.add('primary-action');
            openPrefsBtn?.style.setProperty('display', 'none', 'important');
            
            // Hide the guides if enabled
            document.getElementById('ios-guide').style.display = 'none';
            document.getElementById('mac-guide').style.display = 'none';
        } else {
            openSafariBtn?.style.removeProperty('display');
            openSafariBtn?.classList.remove('primary-action');
            openSafariBtn?.classList.add('secondary-action');
            openPrefsBtn?.style.removeProperty('display');
            
            // Restore guide visibility based on platform
            if (isIOS) {
                document.getElementById('ios-guide').style.display = 'block';
                document.getElementById('mac-guide').style.display = 'none';
            } else {
                document.getElementById('mac-guide').style.display = 'block';
                document.getElementById('ios-guide').style.display = 'none';
            }
        }
    } else {
        document.body.classList.remove(`state-on`);
        document.body.classList.remove(`state-off`);
    }
}

function openPreferences() {
    webkit.messageHandlers.controller.postMessage("open-preferences");
}

function openSafari() {
    webkit.messageHandlers.controller.postMessage("open-safari");
}

const openPreferencesButton = document.querySelector("button.open-preferences");
if (openPreferencesButton) {
    openPreferencesButton.addEventListener("click", openPreferences);
}

const openSafariButton = document.querySelector("button.open-safari");
if (openSafariButton) {
    openSafariButton.addEventListener("click", openSafari);
}

const langSwitcher = document.getElementById("appLangSwitcher");
if (langSwitcher) {
    langSwitcher.value = resolveLocale();
    langSwitcher.addEventListener("change", (e) => {
        localStorage.setItem('userLanguage', e.target.value);
        applyLocalizedCopy(window.currentPlatform !== 'macOS');
    });
}

// Fallback if show() is not called quickly
setTimeout(() => {
    if (!document.getElementById('ios-guide').style.display && !document.getElementById('mac-guide').style.display) {
        show(null, false, 'macOS'); // Default to macOS safely
    }
}, 500);
